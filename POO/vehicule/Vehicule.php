<?php

/**
 * Création de la classe Vehicule
 */
abstract class Vehicule
{
// ---------------------------------------------------------------------------------------------------------------
  /**
   * Marque du véhicule
   * @var string
   */
  private $marque;
// ---------------------------------------------------------------------------------------------------------------
  /**
   * Nombre de roues du véhicule
   * @var int
   */
  private $nombreRoues;
// ---------------------------------------------------------------------------------------------------------------
  /**
   * Type du carburant du véhicule (essence ou diesel)
   * @var string
   */
  private $carburant;
// ---------------------------------------------------------------------------------------------------------------
  /**
   * Vitesse maximum du véhicule
   * @var int
   */
  private $vitesseMax;
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------  
  /**
   * Getter de l'attribut $marque
   * @return string
   */
  public function getMarque(){
    return $this->marque;
  }
// ---------------------------------------------------------------------------------------------------------------
  /**
   * Setter de l'attribut $marque
   */
  public function setMarque($value){
    $this->marque = $value;
    return $this;
  }
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------
/**
 * Getter de l'attribut $nombreRoues
 * @return int
 */
public function getNombreRoues(){
  return $this->nombreRoues;
}
// ---------------------------------------------------------------------------------------------------------------
/**
 * Setter de l'attribut $nombreRoues
 */
public function setNombreRoues($value){
  $this->nombreRoues = $value;
  return $this;
}
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------
/**
 * Getter de l'attribut $carburant
 * @return string
 */
public function getCarburant(){
  return $this->carburant;
}
// ---------------------------------------------------------------------------------------------------------------
/**
 * Setter de l'attribut $carburant
 */
public function setCarburant($value){
  $this->carburant = $value;
  return $this;
}
// ---------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------
/**
 * Getter de l'attribut $vitesseMax
 * @return int
 */
public function getVitesseMax(){
  return $this->vitesseMax;
}
// ---------------------------------------------------------------------------------------------------------------
/**
 * Setter de l'attribut $vitesseMax
 */
public function setVitesseMax($value){
  $this->vitesseMax = $value;
  return $this;
}

}













?>
